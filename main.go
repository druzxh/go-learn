package main

import "fmt"

func main() {
	//init var
	var name string

	// print text
	fmt.Println("Hello world")

	// assign var
	name = "Badru"

	// print var
	fmt.Println(name)

	// variable
	iniVariabel := "Isinya variable"
	fmt.Println(iniVariabel)

	// double variable
	var (
		firstName = "Jack"
		lastName  = "Kie"
	)

	// Join var
	fullName := firstName + lastName

	fmt.Println(fullName)
}
